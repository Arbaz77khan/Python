name = "Arbaz"

print(name[1])
print(name[0:3])  # O se lekar 3 tak..Excluding 3

print(name[1:4])   # 1 se lekar 4 tak..Excluding 4

print(name[:5])   #Same as name[0:5]

print(name[0:])  #Same as name[0:5]

print(name[-4:-1])  #Same as name[1:5]

print(name[0:5:2]) #last value will print each 2nd letter.

print(type(name))